let dropMenu2 = document.getElementById("dropMenu2");
let navItemDrop2 = document.getElementById("navItemDrop2");

dropMenu2.addEventListener("mouseenter" , function(){
    navItemDrop2.style.backgroundColor = "#3b1d82";
    navItemDrop2.style.color = "white";
})

dropMenu2.addEventListener("mouseleave" , function(){
    navItemDrop2.style.backgroundColor = "white";
    navItemDrop2.style.color = "#3b1d82";
})

navItemDrop2.addEventListener("mouseenter" , function(){
    navItemDrop2.style.backgroundColor = "#3b1d82";
    navItemDrop2.style.color = "white";
})

navItemDrop2.addEventListener("mouseleave" , function(){
    navItemDrop2.style.backgroundColor = "white";
    navItemDrop2.style.color = "#3b1d82";
})